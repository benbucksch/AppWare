//#define ICONBACKTOP (PIXELWNDTOP+PIXELWNDHEIGHT-ICONBACKHEIGHT)
#define ICONBACKTOP (PIXELWNDTOP+PIXELWNDHEIGHT/2-(2*ICONBORDERWIDTH+PIXELCOUNT)/2)
#define ICONBACKWIDTH (2*ICONBORDERWIDTH+PIXELCOUNT)

#define ICONLEFT (ICONBACKLEFT+ICONBORDERWIDTH)
