#define LEFTBUTTONLEFT (ICONBACKLEFT)
#define LEFTBUTTONTOP (ICONBACKTOP+ICONBACKHEIGHT+PIXELWNDPALETTESPACE)

#define RIGHTBUTTONLEFT (LEFTBUTTONLEFT+BUTTONWIDTH+1)
