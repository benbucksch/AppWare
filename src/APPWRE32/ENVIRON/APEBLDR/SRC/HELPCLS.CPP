/////////////////////////////////////////////////////////////////////////////
// FUNCTION....:  HELPCLS
//
// DESCRIPTION.:  constructor
/////////////////////////////////////////////////////////////////////////////
HELPCLS::HELPCLS(
	HINSTANCE	hInst)
	{
	ClearHelpFlag();
	hHelpCursor = LoadCursor(hInst, "HelpCursor");
	}

/////////////////////////////////////////////////////////////////////////////
// FUNCTION....:  ~HELPCLS
//
// DESCRIPTION.:  destructor
/////////////////////////////////////////////////////////////////////////////
HELPCLS::~HELPCLS()
	{
	}

/////////////////////////////////////////////////////////////////////////////
// FUNCTION....:  GetMenuContextId
//
// DESCRIPTION.:
/////////////////////////////////////////////////////////////////////////////
DWORD HELPCLS::GetMenuContextId(
	WORD MenuID)
	{
  for(int i=0;i<MENUHELPCT;i++)
    if(MenuID ==menuHelpIdTable[i].menuID)
      return menuHelpIdTable[i].helpID;
  return (DWORD)0L;
	}

/////////////////////////////////////////////////////////////////////////////
// FUNCTION....:  GetCategoryContextId
//
// DESCRIPTION.:
/////////////////////////////////////////////////////////////////////////////
DWORD HELPCLS::GetCategoryContextId(
	LPSTR catName)
	{
	for(int i=0;i<OBJHELPCT;i++)
		if(!lstrcmp(catName, (LPSTR)categoryHelpIdTable[i].catName))
			return categoryHelpIdTable[i].catID;
	return (DWORD)0L;
	}
